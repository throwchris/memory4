function toggleTextColor() {
    var textElement = document.getElementById("text");

    if (textElement.classList.contains("w3-text-light-grey")) {
        textElement.classList.remove("w3-text-light-grey");
    } else {
        textElement.classList.add("w3-text-light-grey");
    }
}

